public class MyApp {

    public static void main(String[] args) {
        // 文字
        char a = 'a'; // シングルクォーテーション
        // 整数 byte short int long
        int x = 10;
        long y = 555555555L; // 最後にLをつける
        // 浮動小数点数 float double
        double d = 23423.344;
        float f = 32.33F; // 最後にFをつける
        // 論理値
        boolean flag = true; // or false
        // 文字列
        // \n 改行
        // \t タブ
        String msg = "Hello Wo\nrld Again\t!"; // ダブルクォーテーション

        System.out.println(msg);
    }

}